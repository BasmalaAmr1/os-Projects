bits 16          ; إخبار المجمّع أننا نستخدم نمط 16-بت [cite: 84]

start:
    ; إعداد مقاطع الذاكرة (Memory Segments) [cite: 85, 95]
    mov ax, 0x07C0
    mov ds, ax       ; تحديد مكان البيانات [cite: 106]
    
    ; إعداد الـ BIOS لطباعة الحروف [cite: 110, 112]
    mov si, msg      ; وضع عنوان الرسالة في مسجل SI [cite: 113]
    mov ah, 0x0E     ; كود وظيفة طباعة حرف في BIOS [cite: 113]

.next:
    lodsb            ; تحميل حرف من SI إلى AL وزيادة SI تلقائياً [cite: 117, 128]
    cmp al, 0        ; هل وصلنا لنهاية الرسالة (الصفر)؟ [cite: 118]
    je .done         ; إذا نعم، اذهب للنهاية [cite: 118]
    int 0x10         ; استدعاء الـ BIOS لطباعة الحرف الموجود في AL [cite: 118, 124]
    jmp .next        ; كرر للـ حرف التالي [cite: 119, 124]

.done:
    jmp $            ; حلقة تكرارية لا نهائية لمنع الجهاز من التوقف [cite: 126]

msg: db 'Hello', 0   ; الرسالة متبوعة بـ 0 لتمييز النهاية [cite: 139, 140]

; تعبئة باقي الـ 512 بايت بالأصفار ووضع التوقيع السحري في النهاية [cite: 143, 146]
times 510-($-$$) db 0 
dw 0xAA55            ; توقيع الـ Boot Record [cite: 147]